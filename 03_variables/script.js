// Ways to declare a variable:
// var, let, const
// 'var' was the original declaration, but in 2015 update (ES6 - ECMAScript 6), 'let' and 'const' added.

let firstName = "John";
let lastName = "Doe";
console.log(firstName, lastName);

// Declare multiple values at once
let a, b, c;

const d = 10,
  e = 20,
  f = 30;

// Multi-Word Formating:

// firstName = camelCase

// first_name = snake_case

// FirstName = PascalCase

// first-name = kebab-case

// firstname = flatcase , lowercase

// FIRSTNAME = UPPERFLATCASE , UPPERCASE

// FIRST_NAME = SCREAMING_SNAKE_CASE

// First-Name = Train-Case
